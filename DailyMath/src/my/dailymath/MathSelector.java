    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String radioText1 = "";
        String radioText2 = "";
        Operation op1 = new Operation();
        if (jRadioButton1.isSelected()){
            radioText1 = jRadioButton1.getText();
        }
        if (jRadioButton2.isSelected()){
            radioText1 = jRadioButton2.getText();
        }
        if (jRadioButton3.isSelected()){
            radioText2 = jRadioButton3.getText();
        }
        if (jRadioButton4.isSelected()){
            radioText2 = jRadioButton4.getText();
        }
        if (jRadioButton5.isSelected()){
            radioText2 = jRadioButton5.getText();
        }
        if (radioText1.equals("Easy")){
            if (radioText2.equals("Addition")){
                op1.addition();
            }
            if (radioText2.equals("Subtraction")){
                op1.subtraction();
            }
            if (radioText2.equals("Multiplication")){
                op1.multiplication();
            }
        }
        if (radioText1.equals("Normal")){            
            if (radioText2.equals("Addition")){
                op1.hardAddition();
            }
            if (radioText2.equals("Subtraction")){
                op1.hardSubtraction();
            }
            if (radioText2.equals("Multiplication")){
                op1.hardMultiplication();
            }
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

